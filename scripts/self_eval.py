#!/usr/bin/env python3
"""
self_eval.py — Zeref Weekly Self-Evaluation Engine
Version: 3.0.0
Run: python3 scripts/self_eval.py
Purpose: Reads experience.jsonl, generates weekly upgrade comparison report
Output: wiki/upgrade-reports/zeref-upgrade-report-YYYY-MM-DD.md
"""

import json
import os
from pathlib import Path
from datetime import date, timedelta
from collections import Counter

FLEET_ROOT = Path(__file__).parent.parent
LOG_PATH = FLEET_ROOT / "wiki" / "experience.jsonl"
REPORT_DIR = FLEET_ROOT / "wiki" / "upgrade-reports"
REPORT_DIR.mkdir(parents=True, exist_ok=True)

today = date.today()
week_ago = today - timedelta(days=7)

# ─── Load entries ────────────────────────────────────────────────────────────
entries = []
if LOG_PATH.exists():
    with open(LOG_PATH) as f:
        for line in f:
            line = line.strip()
            if line:
                try:
                    entries.append(json.loads(line))
                except json.JSONDecodeError:
                    pass

this_week = [e for e in entries if e.get("date", "") >= week_ago.isoformat()]
last_week = [e for e in entries if week_ago.isoformat() > e.get("date", "") >= (week_ago - timedelta(days=7)).isoformat()]

# ─── Compute Metrics ──────────────────────────────────────────────────────────
def avg(lst, key):
    vals = [e[key] for e in lst if key in e and e[key] is not None]
    return round(sum(vals) / len(vals), 2) if vals else None

def skill_freq(lst):
    skills = []
    for e in lst:
        skills.extend(e.get("skills_used", []))
    return Counter(skills).most_common(5)

def failure_patterns(lst):
    return [e.get("failure_pattern") for e in lst if e.get("failure_pattern")]

this_avg_quality = avg(this_week, "output_quality_score")
last_avg_quality = avg(last_week, "output_quality_score")
quality_delta = round(this_avg_quality - last_avg_quality, 2) if (this_avg_quality and last_avg_quality) else None

this_routing_acc = round(sum(1 for e in this_week if e.get("routing_correct")) / max(len(this_week), 1) * 100, 1)
this_repair_rate = round(sum(1 for e in this_week if e.get("human_repair_required")) / max(len(this_week), 1) * 100, 1)

# ─── Generate Report ──────────────────────────────────────────────────────────
report_path = REPORT_DIR / f"zeref-upgrade-report-{today.isoformat()}.md"

report = f"""# ZEREF WEEKLY UPGRADE REPORT
**Date:** {today.isoformat()}
**Period:** {week_ago.isoformat()} → {today.isoformat()}
**Generated by:** self_eval.py v3.0.0

---

## This Week at a Glance

| Metric | This Week | Last Week | Delta |
|--------|-----------|-----------|-------|
| Sessions logged | {len(this_week)} | {len(last_week)} | {len(this_week) - len(last_week):+d} |
| Avg output quality | {this_avg_quality or "—"}/10 | {last_avg_quality or "—"}/10 | {f"{quality_delta:+.2f}" if quality_delta else "—"} |
| Routing accuracy | {this_routing_acc}% | — | — |
| Human repair rate | {this_repair_rate}% | — | — |

## Most Used Skills This Week

{"".join(f"- {skill}: {count} sessions\\n" for skill, count in skill_freq(this_week)) or "— No data yet —"}

## Failure Patterns Detected

{"".join(f"- {p}\\n" for p in failure_patterns(this_week)) or "— No failures logged this week —"}

---

## PROPOSED UPGRADES FOR YOUR REVIEW

*Based on the failure patterns and quality trends above, the self-eval engine proposes the following changes.*
*Review each. Approve or reject. All changes are applied only after your explicit approval.*

### Proposed Skill Updates

> AUTO-GENERATED: skill_updater.py will populate this section.
> If no patterns detected, no updates are proposed.

{"- No skill updates proposed this week." if not failure_patterns(this_week) else ""}

### Proposed Routing Updates

> Review routing accuracy. If below 85%, trigger phrases need review.

{"- Routing accuracy is strong this week." if this_routing_acc >= 85 else f"- ⚠️ Routing accuracy {this_routing_acc}% is below 85% threshold. Recommend reviewing trigger phrases in registry."}

---

## YOUR DECISION

For each proposed upgrade:
- ✅ APPROVE — change will be applied by skill_updater.py
- ❌ REJECT — change is logged as rejected and reviewed next cycle
- 🔄 DEFER — revisit next week

---
*Zeref self-improvement is human-approved. No changes apply without your decision.*
"""

with open(report_path, "w") as f:
    f.write(report)

print(f"✅ Weekly report generated: {report_path}")
print(f"   Sessions this week: {len(this_week)}")
print(f"   Avg quality: {this_avg_quality}/10")
print(f"   Open report to review and approve/reject proposed upgrades.")
