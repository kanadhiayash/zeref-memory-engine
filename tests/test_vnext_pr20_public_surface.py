"""vNext PR 20 gate tests — public surface + 2.0-alpha release."""

from __future__ import annotations

import json
import re
from pathlib import Path

import pytest

from zeref.registry import generate_all

REPO_ROOT = Path(__file__).resolve().parents[1]


# ---------------------------------------------------------------------------
# Version consistency across all surfaces
# ---------------------------------------------------------------------------

EXPECTED_VERSION = "2.0.0-alpha.1"


def _read(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def test_zeref_version_file() -> None:
    assert (REPO_ROOT / "zeref" / "VERSION").read_text().strip() == EXPECTED_VERSION


def test_pyproject_version() -> None:
    text = _read(REPO_ROOT / "pyproject.toml")
    assert f'version = "{EXPECTED_VERSION}"' in text


def test_registry_version() -> None:
    reg = json.loads(_read(REPO_ROOT / "zeref-registry.json"))
    assert reg["version"] == EXPECTED_VERSION


def test_plugin_manifest_version() -> None:
    manifest = json.loads(_read(REPO_ROOT / ".claude-plugin" / "plugin.json"))
    assert manifest["version"] == EXPECTED_VERSION


# ---------------------------------------------------------------------------
# Generated registry — regenerated in-place must be deterministic
# ---------------------------------------------------------------------------

REGISTRY_FILES = (
    "components.json", "missions.json", "adapters.json",
    "evaluators.json", "codecs.json", "capabilities.json",
)


def test_generate_all_writes_six_registry_files(tmp_path: Path) -> None:
    # Seed missions + policies dirs for the missions registry to load.
    import shutil
    for name in ("missions", "policies"):
        shutil.copytree(REPO_ROOT / name, tmp_path / name)
    written = generate_all(tmp_path)
    names = {p.name for p in written}
    assert names == set(REGISTRY_FILES)


def test_generated_files_present_in_repo() -> None:
    for name in REGISTRY_FILES:
        assert (REPO_ROOT / "registry" / name).exists(), (
            f"registry/{name} missing — run `python3 -c 'from zeref.registry "
            f"import generate_all; generate_all(\".\")'`"
        )


def test_generated_files_carry_generated_marker() -> None:
    for name in REGISTRY_FILES:
        data = json.loads(_read(REPO_ROOT / "registry" / name))
        assert data.get("$comment", "").startswith("GENERATED BY ZEREF"), (
            f"registry/{name} lacks $comment marker"
        )
        assert data.get("schema"), f"registry/{name} lacks schema"


def test_generated_components_schema_shape() -> None:
    data = json.loads(_read(REPO_ROOT / "registry" / "components.json"))
    ids = {c["id"] for c in data["components"]}
    for expected in ("zeref.storage", "zeref.policy", "zeref.capabilities",
                     "zeref.runtime", "zeref.teams", "zeref.codecs",
                     "zeref.evidence", "zeref.evaluators",
                     "zeref.benchmark.program"):
        assert expected in ids
    for row in data["components"]:
        assert row["status"] in ("runtime", "adapter", "contract",
                                 "experimental")


def test_generated_missions_covers_all_six() -> None:
    data = json.loads(_read(REPO_ROOT / "registry" / "missions.json"))
    ids = {m["id"] for m in data["missions"]}
    assert ids == {"solo", "build", "research", "red", "audit", "ship"}


def test_generated_adapters_reports_all_harnesses() -> None:
    data = json.loads(_read(REPO_ROOT / "registry" / "adapters.json"))
    harness_names = {h["name"] for h in data["harness_adapters"]}
    for expected in ("codex", "claude-code", "gemini-cli", "kimi-code",
                     "hermes", "odysseus", "grok"):
        assert expected in harness_names


# ---------------------------------------------------------------------------
# No unsupported runtime claims (§20 PR-20 gate)
# ---------------------------------------------------------------------------

def test_readme_makes_no_hardcoded_provider_claims() -> None:
    text = _read(REPO_ROOT / "README.md")
    banned = re.compile(
        r"claude-(?:opus|sonnet|haiku|fable|instant)"
        r"|gpt-[0-9]|gemini-[0-9]|codex-gpt"
    )
    # README may reference reasoning classes and harness names, but not
    # provider *model* ids.
    match = banned.search(text)
    assert not match, (
        f"README references provider model id {match.group(0)!r}; "
        f"provider ids belong under zeref/adapters/providers/"
    )
