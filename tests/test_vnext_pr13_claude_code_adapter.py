"""vNext PR 13 gate tests — Claude Code harness adapter."""

from __future__ import annotations

from pathlib import Path

from zeref.adapters.harnesses.claude_code import ClaudeCodeAdapter


def test_claude_code_reports_features() -> None:
    report = ClaudeCodeAdapter().detect()
    for feat in ("CLAUDE.md", "hooks", "subagents", "MCP",
                 "slash_commands", "skills"):
        assert feat in report.supported_features


def test_claude_code_context_only_without_signals(monkeypatch, tmp_path: Path) -> None:
    monkeypatch.delenv("CLAUDE_CODE_VERSION", raising=False)
    monkeypatch.delenv("CLAUDECODE_VERSION", raising=False)
    monkeypatch.setattr("zeref.adapters.harnesses.claude_code.which",
                         lambda cmd: None)
    monkeypatch.setenv("HOME", str(tmp_path))
    report = ClaudeCodeAdapter().detect()
    assert not report.detected
    assert report.enforcement_level.value == "C"


def test_claude_code_embedded_with_env(monkeypatch, tmp_path: Path) -> None:
    monkeypatch.setenv("CLAUDE_CODE_VERSION", "1.2.3")
    monkeypatch.setattr("zeref.adapters.harnesses.claude_code.which",
                         lambda cmd: None)
    monkeypatch.setenv("HOME", str(tmp_path))
    report = ClaudeCodeAdapter().detect()
    assert report.detected
    assert report.enforcement_level.value == "A"


def test_claude_code_exports_CLAUDE_md(tmp_path: Path) -> None:
    written = ClaudeCodeAdapter().export_context(
        tmp_path, objective="Do the thing.",
        permissions={"network": "denied"},
    )
    assert written == [tmp_path / "CLAUDE.md"]
    text = written[0].read_text(encoding="utf-8")
    assert text.startswith("<!-- GENERATED BY ZEREF.")
    assert "Do the thing." in text
    assert '"network": "denied"' in text
